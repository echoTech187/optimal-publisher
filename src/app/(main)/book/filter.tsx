type optionProps = {
    value: string;
    label: string;
}
interface FilterBookProps {
    onUpdateParams: (params: URLSearchParams) => void;
}
const FilterBook: React.FC<FilterBookProps> = ({ onUpdateParams }) => {
    const router = useRouter();
    const pathname = usePathname();
    const queries = useSearchParams();
    const [search, setSearch] = useState<string>("");
    const [catagoriesData, setCatagoryData] = useState<optionProps[]>([]);
    const [category, setCategory] = useState<optionProps | null>(null);
    const [authorsData, setAuthorsData] = useState<optionProps[]>([]);
    const [author, setAuthor] = useState<optionProps | null>(null);

    useEffect(() => {
        const params = new URLSearchParams(Array.from(queries.entries()));
        const addQueryParam = () => {

            if (search !== '') {
                params.set("q", search);
                onUpdateParams(params);
            } else {
                params.delete("q");
                onUpdateParams(params);
            }
            if (category) {
                params.set("category", category.value)
                onUpdateParams(params);
            } else {
                params.delete("category");
                onUpdateParams(params);
            }
            if (author) {
                params.set("author", author.value);
                onUpdateParams(params);
            } else {
                params.delete("author");
                onUpdateParams(params);
            }
            router.push(`${pathname}?${params.toString()}`);


        };
        async function getCatagoryData() {
            const categoriesData: optionProps[] = [];
            const response = await fetch('http://127.0.0.1:8000/api/v1/category');
            const data = await response.json();
            data.data.map((catagory: any) => {
                categoriesData.push({
                    value: catagory.slug,
                    label: catagory.category
                });
            });
            setCatagoryData(categoriesData);
        }
        async function getAutorsData() {
            const authorsData: optionProps[] = [];

            const response = await fetch('http://127.0.0.1:8000/api/v1/author');
            const data = await response.json();
            if(!isEmpty(data.data)){
                data.data.map((author: any) => {
                    authorsData.push({
                        value: author.slug,
                        label: author.author_name + ", " + author.author_title
                    });
                });
                setAuthorsData(authorsData);
            }
        }
        getCatagoryData();
        getAutorsData();
        addQueryParam();
    }, [author, category, onUpdateParams, pathname, queries, router, search]);

    return (<>

        <div className="w-78 px-4 hidden md:block">
            <h2 className='text-xl font-bold text-[var(--primary)] mb-8'>Filter</h2>
            <SearchBook search={search} setSearch={setSearch} />
            <div className="mb-4">
                <label htmlFor="category" className="block text-sm font-medium text-gray-700">
                    Kategori
                </label>
                <SelectOption
                    isClearable={true}
                    options={catagoriesData}
                    value={category}
                    onChange={(e) => setCategory(e as optionProps | null)}
                    className="react-select-input"
                    id="category"
                    name="category"
                />
            </div>
            <div className="mb-4">
                <label htmlFor="author" className="block text-sm font-medium text-gray-700">
                    Penulis
                </label>
                <SelectOption
                    isClearable={true}
                    options={authorsData}
                    value={author}
                    onChange={(e) => setAuthor(e as optionProps | null)}
                    className="react-select-input"
                    id="author"
                    name="author"
                />
            </div>
        </div>

    </>);
}

export { FilterBook };